def location_exists?(location, rows)
  if location[:row] > rows.size - 1
    return false
  end

  if location[:column] > rows.first.size - 1
    return false
  end

  true
end

def past_bottom?(location, rows)
  location[:row] > rows.size - 1
end

def past_edge?(location, rows)
  location[:column] > rows.first.size - 1
end

def run(input, slope)
  rows = input.split("\n")
  location = { row: 0, column: 0 }
  trees_found = 0

  until past_bottom?(location, rows)
    if past_edge?(location, rows)
      rows.map! { |row| row += row }
    end

    if rows[location[:row]][location[:column]] == '#'
      trees_found += 1
    end

    location = {
      row: location[:row] + slope[:y],
      column: location[:column] + slope[:x]
    }
  end

  trees_found
end

